public class ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(10 + 5);
        System.out.println(10 - 5);
        System.out.println(10 * 5);
        System.out.println(9 / 2);
        System.out.println(9.0 / 2);
        System.out.println(9 / 2.0);
        System.out.println(10 % 3);
        
    }
}
